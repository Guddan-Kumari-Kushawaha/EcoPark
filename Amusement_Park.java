    private void jButton36ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton36ActionPerformed
        try{
         PreparedStatement ps= con.prepareStatement("select * from foodcourt");
        rs=ps.executeQuery();               
        while(Databasef.getRowCount()>0)
        {
            ((DefaultTableModel)Databasef.getModel()).removeRow(0);
        }
        int columns=rs.getMetaData().getColumnCount();
        while(rs.next())
        {
            Object row[]=new Object[columns];
            for(int i=0; i<columns; i++)
                row[i]=rs.getObject(i+1);
            ((DefaultTableModel)Databasef.getModel()).insertRow(rs.getRow()-1,row);
        }
       } 
       catch(Exception e)
        {
                   e.printStackTrace();
               }
    }//GEN-LAST:event_jButton36ActionPerformed

